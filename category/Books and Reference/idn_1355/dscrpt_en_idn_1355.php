EBookDroid is an open source (GPLed) document viewer for Android based on the VuDroid code base.<br>
Supported formats:<br>
PDF, DjVu, XPS (OpenXPS), Comic books (cbz,cbr), FictionBook (fb2, fb2.zip)
<br>
Supported internet protocols for collaboration with electronic publications sites:
OPDS (http://en.wikipedia.org/wiki/OPDS)
<br>
If you find any issues or problems, please inform us here:
http://code.google.com/p/ebookdroid/<br>
